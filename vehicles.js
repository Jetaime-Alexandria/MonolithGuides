var categories = ["Compact","Luxury","Miscellaneous","Muscle","Rally","SUV","Sedan","Sports","Super","Truck"]

for(a = 0; a < categories.length; a++){
    var main = document.getElementById("list");
    var content = document.createElement("div");
    content.className = "categories";
    content.innerHTML = `
    <h1>${categories[a]}</h1>
    <ul id="${categories[a]}"><ul>
    `;
    main.appendChild(content);
}

for(b = 0; b < vehicles.length; b++){
    var exclusiveCar = vehicles[b].data[0].exclusive[0];
    var category = document.getElementById(`${vehicles[b].type}`);
    var vehicle = document.createElement("li");
    vehicle.setAttribute("onclick",`vehicleData(${vehicles[b].id})`);
    vehicle.id = vehicles[b].id;
    vehicle.className = `${ exclusiveCar.exclusive_vehicle ? 'vehicle exclusive' : 'vehicle'}`
    vehicle.innerHTML = `<a style="text-decoration: none; color: inherit;">${vehicles[b].name}</a>`
    category.appendChild(vehicle);
}

function vehicleData(id) {
    var vehinfo = document.getElementById("vehinfo");
    var exclusiveCar = vehicles[id].data[0].exclusive[0];
    let n = exclusiveCar.exclusive_vehicle ? exclusiveCar.exclusive_vehicle_price : vehicles[id].price;
    let str = n.toLocaleString("en-US");

    vehinfo.innerHTML = `<b>${vehicles[id].name}</b><hr><b>Top Speed:</b> ${vehicles[id].data[0].top_speed}<br><b>Fuel Type:</b> ${vehicles[id].data[0].fuel_type}<br><b>Price:</b> $${str}`;
}